// Deterministic delivery ratios per client (avoid random at runtime)
const DELIVERY_RATIOS: Record<string, number> = {
  acme: 0.75,
  techflow: 0.55,
  greenleaf: 0.88,
};

const mkScope = (clientId: string) =>
  (["social", "seo", "email", "paid"] as const).flatMap((mod) =>
    DEFAULT_SCOPE_TEMPLATES[mod].map((tpl, i) => {
      const qty = parseInt(tpl.unit || "0") || 0;
      return {
        id: `${clientId}-${mod}-${i}`,
        module: mod,
        label: tpl.label,
        unit: tpl.unit,
        delivered: Math.floor(qty * (DELIVERY_RATIOS[clientId] ?? 0.6)),
      };
    }),
  );

